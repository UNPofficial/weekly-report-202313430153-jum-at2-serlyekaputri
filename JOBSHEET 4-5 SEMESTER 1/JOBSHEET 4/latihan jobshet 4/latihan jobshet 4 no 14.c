#include <stdio.h>

int main() {
    int a = 4;

    // increment a
    a++;
    printf("a++ = %d\n", a);

    // increment a
    ++a;
    printf("++a = %d\n", a);

    // decrement a
    a--;
    printf("a-- = %d\n", a);

    // decrement a
    --a;
    printf("--a = %d\n", a);

    return 0;
}
